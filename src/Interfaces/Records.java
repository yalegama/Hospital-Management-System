       public void tableload(){//loading all of data in table
    
        try {
            String sql="SELECT id,name,number,age,blood,address,any FROM patient";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
