    public void tableload(){  // this is for table loading in front of user
    
        try {
            String sql="SELECT id,doctor_id,name,specilization,contact_number,extra,any,hours FROM doctor";
            pst=conn.prepareStatement(sql);
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error");// Error massage for table data
        }
    
    }
